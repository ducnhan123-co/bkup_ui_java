    private void jLabel27MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel27MouseClicked
                                        
     try {
        // Ki·ªÉm tra xem c√≥ d√≤ng n√†o ƒë∆∞·ª£c ch·ªçn kh√¥ng
        int selectedRow = jTable7.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Vui l√≤ng ch·ªçn m·ªôt s·∫£n ph·∫©m ƒë·ªÉ s·ª≠a!");
            return;
        }

        // L·∫•y m√£ s·∫£n ph·∫©m t·ª´ c·ªôt 0 trong b·∫£ng
        String maSP = jTable7.getValueAt(selectedRow, 0).toString();

        // L·∫•y danh s√°ch s·∫£n ph·∫©m t·ª´ BLL
        ArrayList<SanPhamDTO> danhSachSanPham = SanPhamBLL.getDanhSachSanPham();

        // T√¨m s·∫£n ph·∫©m c·∫ßn s·ª≠a
        SanPhamDTO sanPham = null;
        for (SanPhamDTO sp : danhSachSanPham) {
            if (sp.getMaSP().equals(maSP)) {
                sanPham = sp;
                break;
            }
        }

        // Ki·ªÉm tra xem s·∫£n ph·∫©m c√≥ t·ªìn t·∫°i kh√¥ng
        if (sanPham == null) {
            JOptionPane.showMessageDialog(null, "Kh√¥ng t√¨m th·∫•y s·∫£n ph·∫©m v·ªõi m√£: " + maSP);
            return;
        }

        // M·ªü popup s·ª≠a s·∫£n ph·∫©m
        SuaSanPhamPopUp popup = new SuaSanPhamPopUp(sanPham);
        popup.setVisible(true);

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "L·ªói khi m·ªü popup: " + e.getMessage());
    }


    }//GEN-LAST:event_jLabel27MouseClicked

